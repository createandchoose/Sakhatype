import random

# Частотный словарь (топовые слова русского языка, без редких)
# Можно расширить хоть до 1000 слов.
WORDS = [
    "время", "человек", "год", "жизнь", "день", "рука", "глаз", "дом", "работа",
    "слово", "место", "лицо", "друг", "мир", "мысль", "ночь", "ребёнок", "сила",
    "вода", "дорога", "вопрос", "страна", "мама", "мужчина", "женщина", "город",
    "земля", "любовь", "душа", "сердце", "свет", "звук", "ветер", "море", "путь",
    "взгляд", "тишина", "память", "сон", "смех", "ветка", "тень", "часы", "камень",
    "солнце", "река", "тело", "руки", "слёзы", "мы", "я", "они", "он", "она"
]

def generate_monkeytype_text(word_count=50):
    words = [random.choice(WORDS) for _ in range(word_count)]
    
    # Вставляем знаки препинания с вероятностью
    for i in range(5, len(words), random.randint(8, 15)):
        words[i] += random.choice([",", ".", ""])
    
    # Первое слово — с большой буквы
    words[0] = words[0].capitalize()
    
    text = " ".join(words)
    
    # Добавляем точку в конце
    if not text.endswith("."):
        text += "."
    
    return text
